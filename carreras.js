        // Animaci√≥n del t√≠tulo
        function typeWriter(text, element, speed = 100) {
            let i = 0;
            element.innerHTML = '';
            element.style.opacity = '1';
            
            function type() {
                if (i < text.length) {
                    element.innerHTML += text.charAt(i);
                    i++;
                    setTimeout(type, speed);
                }
            }
            type();
        }

